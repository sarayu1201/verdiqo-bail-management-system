// Helper functions for easy querying
export function getCaseByNo(caseNo) {
    return db.bailApplications.find(c => c.caseNo === caseNo);
}

export function getAccusedByCase(caseNo) {
    return db.accusedPersons.find(a => a.caseNo === caseNo);
}

export function getSuretyByCase(caseNo) {
    return db.suretyDetails.find(s => s.caseNo === caseNo);
}

export function getBiometricByCase(caseNo) {
    return db.biometricIdentity.find(b => b.caseNo === caseNo && b.role === 'Accused');
}

