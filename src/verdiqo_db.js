export function getOpenFraudAlerts() {
    return db.fraudAlerts.filter(a => a.alertStatus === 'OPEN' || a.alertStatus === 'UNDER REVIEW');
}

export function getCasesByStatus(status) {
    return db.bailApplications.filter(c => c.applicationStatus === status);
}

export function getHighRiskCases() {
    return db.bailApplications.filter(c => {
        const score = parseInt(c.aiRiskScore0100 || c.aiRiskScore010 || "0");
        return score >= 80;
    });
}

export function getFullCaseProfile(caseNo) {
    const application = getCaseByNo(caseNo);
    if (!application) return null;
    const accused = getAccusedByCase(caseNo);
    const surety = getSuretyByCase(caseNo);
    const biometrics = db.biometricIdentity.filter(b => b.caseNo === caseNo);
    const activeAlerts = db.fraudAlerts.filter(a => a.caseNos && a.caseNos.includes(caseNo));
    
    return {
        application,
        accused,
        surety,
        biometrics,
        activeAlerts
    };
}

